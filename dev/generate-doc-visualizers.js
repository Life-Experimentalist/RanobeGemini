const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..");
const packagePath = path.join(repoRoot, "package.json");
const landingScriptPath = path.join(repoRoot, "landing", "script.js");
const handlersDir = path.join(repoRoot, "src", "utils", "website-handlers");
const outputPath = path.join(
	repoRoot,
	"docs",
	"overview",
	"VISUAL_DASHBOARD.md",
);

function readJson(filePath) {
	return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function parseStatusEntries(sourceText, arrayName) {
	const arrayRegex = new RegExp(
		`const\\s+${arrayName}\\s*=\\s*\\[(.*?)\\n\\];`,
		"s",
	);
	const match = sourceText.match(arrayRegex);
	if (!match) return [];

	const block = match[1];
	const entries = [];
	const entryRegex = /name:\s*"([^"]+)"[\s\S]*?status:\s*"([^"]+)"/g;
	let entryMatch = null;
	while ((entryMatch = entryRegex.exec(block)) !== null) {
		entries.push({ name: entryMatch[1], status: entryMatch[2] });
	}
	return entries;
}

function groupByStatus(items) {
	return items.reduce((acc, item) => {
		acc[item.status] = (acc[item.status] || 0) + 1;
		return acc;
	}, {});
}

function getHandlerCount() {
	const files = fs.readdirSync(handlersDir);
	return files.filter((file) => file.endsWith("-handler.js")).length;
}

function toPieMermaid(title, grouped) {
	const rows = Object.entries(grouped)
		.sort((a, b) => a[0].localeCompare(b[0]))
		.map(([key, value]) => `    "${key}" : ${value}`)
		.join("\n");
	return [`pie title ${title}`, rows].join("\n");
}

function buildDashboard() {
	const pkg = readJson(packagePath);
	const landingScript = fs.readFileSync(landingScriptPath, "utf8");
	const browsers = parseStatusEntries(landingScript, "BROWSERS");
	const sites = parseStatusEntries(landingScript, "SITES");

	const browserGrouped = groupByStatus(browsers);
	const siteGrouped = groupByStatus(sites);
	const handlerCount = getHandlerCount();
	const generatedAt = new Date().toISOString();

	const browserRows = browsers
		.map((browser) => `| ${browser.name} | ${browser.status} |`)
		.join("\n");
	const siteRows = sites
		.map((site) => `| ${site.name} | ${site.status} |`)
		.join("\n");

	return `# Visual Dashboard\n\n> Auto-generated by npm run docs:visualize. Do not edit manually.\n\n- Generated at: ${generatedAt}\n- Package version: ${pkg.version}\n- Registered website handlers: ${handlerCount}\n\n## Browser Support Status\n\n~~~mermaid\n${toPieMermaid("Browser Support Status", browserGrouped)}\n~~~\n\nDiagram elements:\n- ready: published and install-ready browser channels\n- coming: planned browser channels\n- paused: temporarily paused channels\n\n| Browser | Status |\n| --- | --- |\n${browserRows || "| n/a | n/a |"}\n\n## Website Support Status\n\n~~~mermaid\n${toPieMermaid("Website Support Status", siteGrouped)}\n~~~\n\nDiagram elements:\n- ready: handler currently active\n- coming: handler planned but not shipped\n- paused: handler intentionally disabled\n\n| Site | Status |\n| --- | --- |\n${siteRows || "| n/a | n/a |"}\n\n## Delivery Topology\n\n~~~mermaid\ngraph TD\n    A[True Web PWA] --> B[Library Hub]\n    B --> C[Extension Library]\n    C --> D[Website Content Controls]\n    B --> E[Drive Backup Sync]\n    C --> E\n~~~\n\nDiagram elements:\n- A: installable web app entry on HTTPS\n- B: web app launch surface for library workflows\n- C: extension-backed full library runtime\n- D: in-page enhance and summarize controls\n- E: cross-surface sync bridge\n`;
}

function main() {
	const dashboard = buildDashboard();
	fs.writeFileSync(outputPath, dashboard, "utf8");
	console.log(`Updated ${path.relative(repoRoot, outputPath)}`);
}

main();
